package javaBasics;

public class Variables {
	public static void main(String[] args) {
		int num = 4;
		float num1 = 4.22f;
		char letter = 'D';
		boolean bool = true;
		double pi = 3.14;
		String text = "Hiii";
		System.out.println(num);
		System.out.println(num1);
		System.out.println(letter);
		System.out.println(bool);
		System.out.println(pi);
		System.out.println(text);
	}

}
